//add
function addtwonumber(One , Tow){
    return One + Tow
}
console.log (addtwonumber(18,3))

//sub

let num = function(one , tow){
    return one - tow;
}
console.log (num(20,6))

// multi
let multi = function(oneNumber , towNumber){
    return oneNumber * towNumber;
}
console.log (multi(2,6))

